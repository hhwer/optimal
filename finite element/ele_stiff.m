function Ke =  ele_stiff()
lambda = 3.65e04;
mu = 1.5250e04;
Ke = cell(2,1);
T1 = [0,-1;1,0;-1,1];
Ke{1} = lambda*([T1(:,1);T1(:,2)]*[T1(:,1);T1(:,2)]');
Ke{1}(1:3,1:3) = Ke{1}(1:3,1:3) + mu*(T1*T1');
Ke{1}(4:6,4:6) = Ke{1}(4:6,4:6) + mu*(T1*T1');
Ke{1} = Ke{1} + mu*[T1(:,1)*T1(:,1)',T1(:,2)*T1(:,1)';...
                T1(:,1)*T1(:,2)',T1(:,2)*T1(:,2)'];
Ke{1} = Ke{1}/2;
T2 = [-1,0;1,-1;0,1];
Ke{2} = lambda*([T2(:,1);T2(:,2)]*[T2(:,1);T2(:,2)]');
Ke{2}(1:3,1:3) = Ke{2}(1:3,1:3) + mu*(T2*T2');
Ke{2}(4:6,4:6) = Ke{2}(4:6,4:6) + mu*(T2*T2');
Ke{2} = Ke{2} + mu*[T2(:,1)*T2(:,1)',T2(:,2)*T2(:,1)';...
                T2(:,1)*T2(:,2)',T2(:,2)*T2(:,2)'];
Ke{2} = Ke{2}/2;
end
