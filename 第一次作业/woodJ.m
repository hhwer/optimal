function [r J] = woodJ(x)
%WOODJ Wood function with output r J
% 
% (-3, -1, -3, -1) for test
% min = 0 at (1, 1, 1, 1)

% Version:	2009.05.22
% Create:	2009.05.22
% Coder:	Xin Liang
% Bug Submission:	liangxinslm@163.com

x = x(1:4);

r = [10*(x(2)-x(1)^2);...
    1-x(1);...
    sqrt(90)*(x(4)-x(3)^2);...
    1-x(3);...
    sqrt(10)*(x(2)+x(4)-2);...
    sqrt(10)\(x(2)-x(4))];
J = [-20*x(1) 10 0 0;...
    -1 0 0 0;...
    0 0 -2*sqrt(90)*x(3) sqrt(90);...
    0 0 -1 0;...
    0 sqrt(10) 0 sqrt(10);...
    0 1/sqrt(10) 0 1/sqrt(10)];