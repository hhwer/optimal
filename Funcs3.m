%UNTITLED example3
%   此处显示详细说明

F = @(x1,x2,x3,x4) x1^2+x2^2+2*x3^2+x4^2-5*x1-5*x2-21*x3+7*x4;
h2 = @(x1,x2,x3,x4) -x1^2-x2^2-x3^2-x4^2-x1+x2-x3+x4+8;
h3 = @(x1,x2,x3,x4) -x1^2-2*x2^2-x3^2-2*x4^2+x1+x4+10;
h4 = @(x1,x2,x3,x4) -x1^2-x2^2-x3^2-2*x1+x2+x4+5;
f2 = @(x1,x2,x3,x4) F(x1,x2,x3,x4)-10*h2(x1,x2,x3,x4);
f3 = @(x1,x2,x3,x4) F(x1,x2,x3,x4)-10*h3(x1,x2,x3,x4);
f4 = @(x1,x2,x3,x4) F(x1,x2,x3,x4)-10*h4(x1,x2,x3,x4);
f = [F(x(1),x(2),x(3),x(4)),f2(x(1),x(2),x(3),x(4)),f3(x(1),x(2),x(3),x(4)),f4(x(1),x(2),x(3),x(4))]';
g1 = @(x1,x2,x3,x4) [2*x1-5, 2*x2-5, 4*x3-21, 2*x4+7]';
g2 = @(x1,x2,x3,x4) [-2*x1-1,-2*x2+1, -2*x3-1, -2*x4+1]';
g3 = @(x1,x2,x3,x4) [-2*x1+1, -4*x2, -2*x3, -4*x4+1]';
g4 = @(x1,x2,x3,x4) [-2*x1-2, -2*x2+1, -2*x3, 1]';
g0 = g1(x(1),x(2),x(3),x(4));
g = {g0,g0-10*g2(x(1),x(2),x(3),x(4)),g0-10*g3(x(1),x(2),x(3),x(4)),g0-10*g4(x(1),x(2),x(3),x(4))};
G1 = [2,0,0,0;0,2,0,0;0,0,4,0;0,0,0,2];
G2 = [-2,0,0,0;0,-2,0,0;0,0,-2,0;0,0,0,-2];
G3 = [-2,0,0,0;0,-4,0,0;0,0,-2,0;0,0,0,-4];
G4 = [-2,0,0,0;0,-2,0,0;0,0,-2,0;0,0,0,0];
G = {G1,G1-10*G2,G1-10*G3,G1-10*G4};

end

